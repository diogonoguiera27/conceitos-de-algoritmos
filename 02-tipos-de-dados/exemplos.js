// ======================================================
// EXEMPLOS - TIPOS DE DADOS
// ======================================================

// Tipo inteiro
let idade = 20;
let quantidadeEstoque = 15;

console.log("Tipo inteiro");
console.log("Idade:", idade);
console.log("Quantidade em estoque:", quantidadeEstoque);

console.log("-----------------------------");

// Tipo real
let altura = 1.75;
let preco = 149.90;
let nota = 8.5;

console.log("Tipo real");
console.log("Altura:", altura);
console.log("Preço:", preco);
console.log("Nota:", nota);

console.log("-----------------------------");

// Tipo caractere
// Em JavaScript, caractere é representado como string com apenas um símbolo.

let letra = "A";
let inicialNome = "J";
let simbolo = "#";

console.log("Tipo caractere");
console.log("Letra:", letra);
console.log("Inicial do nome:", inicialNome);
console.log("Símbolo:", simbolo);

console.log("-----------------------------");

// Tipo texto ou cadeia de caracteres

let nome = "José";
let cidade = "Senador Canedo";
let produto = "Anel de Prata";

console.log("Tipo texto");
console.log("Nome:", nome);
console.log("Cidade:", cidade);
console.log("Produto:", produto);

console.log("-----------------------------");

// Tipo lógico

let estaEstudando = true;
let produtoDisponivel = false;

console.log("Tipo lógico");
console.log("Está estudando?", estaEstudando);
console.log("Produto disponível?", produtoDisponivel);