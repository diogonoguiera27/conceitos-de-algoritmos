// Operadores aritméticos
// São usados para fazer cálculos matemáticos.

let numero1 = 10;
let numero2 = 5;

let soma = numero1 + numero2;
let subtracao = numero1 - numero2;
let multiplicacao = numero1 * numero2;
let divisao = numero1 / numero2;
let restoDivisao = numero1 % numero2;

console.log("Operadores aritméticos");
console.log("Número 1:", numero1);
console.log("Número 2:", numero2);

console.log("Soma:", soma);
console.log("Subtração:", subtracao);
console.log("Multiplicação:", multiplicacao);
console.log("Divisão:", divisao);
console.log("Resto da divisão:", restoDivisao);

console.log("-----------------------------");

// Exemplo prático: calcular valor total de uma compra

let precoProduto = 50;
let quantidade = 3;

let valorTotal = precoProduto * quantidade;

console.log("Exemplo prático - Compra");
console.log("Preço do produto:", precoProduto);
console.log("Quantidade:", quantidade);
console.log("Valor total:", valorTotal);

console.log("-----------------------------");

// Exemplo prático: calcular média de notas

let nota1 = 8;
let nota2 = 7;
let nota3 = 9;

let media = (nota1 + nota2 + nota3) / 3;

console.log("Exemplo prático - Média");
console.log("Nota 1:", nota1);
console.log("Nota 2:", nota2);
console.log("Nota 3:", nota3);
console.log("Média:", media);